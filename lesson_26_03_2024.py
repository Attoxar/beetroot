# scrum
# 1. short development cycles ( normaly one or a few weeks)
# 2 each cycle starts with a preperation meeting ( discussing plans for the next cycle)
# 3 each day team starts have a "scrum" meeting - no more than 15 min
# 4. two more meetings at the end of each sprint: wrap up meeting (disscussing what we have accomplished and what we haven't)
# 5. the last of the meetings (ceremonies) - retrospective.